    private void btn_acceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_acceptActionPerformed
        // TODO add your handling code here:
        switch (evt.getActionCommand()) {
            case "Aceptar":
                Estudiante estudiante = new Estudiante();
                estudiante.setName(this.text_name.getText());
                estudiante.setSurname(this.text_surname.getText());
                estudiante.setPhone(this.text_phone.getText());
                if (this.rb_man.isSelected()) {
                    estudiante.setSex("Hombre");
                } else {
                    estudiante.setSex("Mujer");
                }
                estudiante.setDirection(this.text_direction.getText());
                list_studen.add(estudiante);
                System.out.println(list_studen.get(0).toString() + list_studen.size());
                
                break;

        }


    }//GEN-LAST:event_btn_acceptActionPerformed

